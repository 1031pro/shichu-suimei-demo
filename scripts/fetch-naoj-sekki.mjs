import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const START_YEAR = Number.parseInt(process.argv[2] ?? '1900', 10);
const END_YEAR = Number.parseInt(process.argv[3] ?? '2200', 10);

const PROJECT_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const OUT_DIR = path.join(PROJECT_ROOT, 'data', 'setsuiri');
const SOURCE_URL = 'https://eco.mtk.nao.ac.jp/cgi-bin/koyomi/cande/phenomena_sy.cgi';

const SETSU_NAMES = new Set([
  '小寒',
  '立春',
  '啓蟄',
  '清明',
  '立夏',
  '芒種',
  '小暑',
  '立秋',
  '白露',
  '寒露',
  '立冬',
  '大雪',
]);

const EXPECTED_ORDER = [
  '小寒',
  '立春',
  '啓蟄',
  '清明',
  '立夏',
  '芒種',
  '小暑',
  '立秋',
  '白露',
  '寒露',
  '立冬',
  '大雪',
];

function usage() {
  console.log('Usage: node scripts/fetch-naoj-sekki.mjs [startYear] [endYear]');
  console.log('Example: node scripts/fetch-naoj-sekki.mjs 1900 2200');
}

function assertYearRange(startYear, endYear) {
  if (!Number.isInteger(startYear) || !Number.isInteger(endYear)) {
    usage();
    throw new Error('Year arguments must be integers.');
  }
  if (startYear < -2999 || endYear > 2999 || startYear > endYear) {
    usage();
    throw new Error('Year range must be within -2999..2999 and start <= end.');
  }
}

function decodeHtml(buffer) {
  return new TextDecoder('euc-jp').decode(buffer);
}

function stripTags(value) {
  return value.replace(/<[^>]*>/g, '').trim();
}

function parseRows(html, year) {
  const rows = [...html.matchAll(/<tr><td>(\d{4}\/\d{2}\/\d{2})<\/td>\s*<td>(\d{2}:\d{2})<\/td>\s*<td>太陽<\/td>\s*<td>二十四節気<\/td>\s*<td><\/td>\s*<td>([^<]+)\(黄経(\d+)°\)<\/td><\/tr>/g)];
  const events = rows.map((match) => {
    const [, date, time, name, longitude] = match;
    const [yyyy, month, day] = date.split('/').map(Number);
    const [hour, minute] = time.split(':').map(Number);
    return {
      year: yyyy,
      month,
      day,
      hour,
      minute,
      name: stripTags(name),
      longitude: Number.parseInt(longitude, 10),
    };
  });

  const setsu = events.filter((event) => SETSU_NAMES.has(event.name));
  if (setsu.length !== 12) {
    throw new Error(`Expected 12 setsu rows for ${year}, got ${setsu.length}.`);
  }

  const actualOrder = setsu.map((event) => event.name);
  if (actualOrder.join(',') !== EXPECTED_ORDER.join(',')) {
    throw new Error(`Unexpected setsu order for ${year}: ${actualOrder.join(',')}`);
  }

  return setsu;
}

async function fetchYear(year) {
  const body = new URLSearchParams({
    year: String(year),
    lst: '9',
    phenom: '50',
    cal: '0',
    jg: '2',
    dtm: '0',
    dt: '0',
    body: '0',
    coord: '1',
    figure: '0',
  });

  const response = await fetch(SOURCE_URL, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/x-www-form-urlencoded',
      'User-Agent': 'shichu-suimei-tool data generator',
    },
    body,
  });

  if (!response.ok) {
    throw new Error(`NAOJ request failed for ${year}: ${response.status} ${response.statusText}`);
  }

  const html = decodeHtml(await response.arrayBuffer());
  return parseRows(html, year);
}

function toLegacyArray(events) {
  return events.map((event) => [event.year, event.month, event.day, event.hour, event.minute]);
}

function toJson(events) {
  return {
    source: {
      name: '国立天文台 暦計算室 二十四節気・雑節 長期版',
      url: SOURCE_URL,
      timezone: 'JST UTC+9',
      fetchedAt: new Date().toISOString(),
      notes: [
        '節月切替用に二十四節気のうち12個の節のみを抽出しています。',
        '抽出対象: 小寒, 立春, 啓蟄, 清明, 立夏, 芒種, 小暑, 立秋, 白露, 寒露, 立冬, 大雪',
      ],
    },
    range: {
      startYear: START_YEAR,
      endYear: END_YEAR,
      years: END_YEAR - START_YEAR + 1,
    },
    events,
    legacyArray: toLegacyArray(events),
  };
}

async function main() {
  assertYearRange(START_YEAR, END_YEAR);
  await mkdir(OUT_DIR, { recursive: true });

  const allEvents = [];
  for (let year = START_YEAR; year <= END_YEAR; year += 1) {
    const events = await fetchYear(year);
    allEvents.push(...events);
    if ((year - START_YEAR + 1) % 25 === 0 || year === END_YEAR) {
      console.log(`Fetched ${year - START_YEAR + 1}/${END_YEAR - START_YEAR + 1} years`);
    }
    await new Promise((resolve) => setTimeout(resolve, 50));
  }

  const json = toJson(allEvents);
  const jsonPath = path.join(OUT_DIR, `setsuiri-${START_YEAR}-${END_YEAR}.json`);
  const legacyPath = path.join(OUT_DIR, `setsuiri-${START_YEAR}-${END_YEAR}.js`);

  await writeFile(jsonPath, `${JSON.stringify(json, null, 2)}\n`, 'utf8');
  await writeFile(
    legacyPath,
    `// Generated by scripts/fetch-naoj-sekki.mjs\n` +
      `// Source: ${SOURCE_URL}\n` +
      `// Timezone: JST UTC+9\n` +
      `export const setsuiri = ${JSON.stringify(json.legacyArray)};\n`,
    'utf8',
  );

  console.log(`Wrote ${json.events.length} events`);
  console.log(jsonPath);
  console.log(legacyPath);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
